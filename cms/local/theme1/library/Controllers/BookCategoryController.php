    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd($request->all());
        $this->validate(
            $request,
            [
                "cat_name" => "required|min:3",
                "category_type" => "required",
            ],
            [
                "cat_name.min" => "Category Name Minimum 3 Characters",
            ]
        );
        DB::beginTransaction();
        try {
            $obj = new BookCategoryModel();
            $obj->cat_name = $request->cat_name;
            $obj->category_type = $request->category_type;

            if ($request->category_image) {
                $obj->category_image = $this->CoverImage(
                    $request->category_image,

                    "school/books/"
                );
            }

            $obj->save();

            DB::commit();
        } catch (\Exception $e) {
            DB::rollback();
            $message = str_replace(
                ["\r", "\n", "'", "`"],
                " ",
                $e->getMessage()
            );
            return redirect()
                ->back()
                ->withInput()
                ->with("exception_error", $message);
        }

        Session::flash("success", "saved successfully");
        return redirect()->route("bookcategory.index");
    }

